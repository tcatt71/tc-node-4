// Arithmetic Operators
1 + 2; // addition
1 - 2; // subtraction
1 * 2; // multiplication
1 / 2; // division
1 % 2; // division remainder
1 ** 2; // exponentiation

// Comparison Operators
1 > 2; // greater than
1 >= 2; // greater than or equal to
1 < 2; // less than
1 <= 2; // less than or equal to
1 == 2; // equal to (loose with type coercion)
1 != 2; // inequal to (loose with type coercion)
1 === 2; // equal to (strict without type coercion)
1 !== 2; // inequal to (strict without type coercion)

// Logical Operators
1 == 2 && 1 > 2; // Logical AND, Both expressions must equate to TRUE
1 == 2 || 1 > 2; // Logical OR, Only one expression must equate to TRUE
